def _detect_local_solvers() -> list[str]:
    """Return names of local solvers that are available on PATH."""
    available = []
    for name, binary in [("CPLEX", "cplex"), ("HiGHS", "highs"), ("GLPK", "glpsol")]:
        if shutil.which(binary):
            available.append(name)
    try:
        import highspy  # type: ignore[import]  # noqa: F401, PLC0415

        if "HiGHS" not in available:
            available.append("HiGHS (Python)")
    except ImportError:
        pass
    return available


# ---------------------------------------------------------------------------
# IIS runner dispatcher
# ---------------------------------------------------------------------------


def run_iis(
    lp_path: Path,
    solver: str,
    email: str,
    neos_url: str,
    timeout: int,
) -> Optional[tuple[bool, str, str]]:
    """
    Try to find the IIS using the requested strategy.

    Returns ``(success, solver_name, output)`` or None if no solver was tried.
    """
    solver_lower = solver.lower()
    tried: list[tuple[str, Any]] = []

    def _try_cplex() -> Optional[tuple[bool, str, str]]:
        ok, out = run_local_cplex(lp_path, timeout=timeout)
        return (ok, "CPLEX (local)", out)

    def _try_highs() -> Optional[tuple[bool, str, str]]:
        # Prefer Python bindings (more info) over binary
        ok, out = run_local_highs_python(lp_path)
        if ok:
            return (ok, "HiGHS (Python)", out)
        ok, out = run_local_highs_binary(lp_path, timeout=timeout)
        return (ok, "HiGHS (binary)", out)

    def _try_glpk() -> Optional[tuple[bool, str, str]]:
        ok, out = run_local_glpk(lp_path, timeout=timeout)
        return (ok, "GLPK", out)

    def _try_neos() -> Optional[tuple[bool, str, str]]:
        if not email:
            return (
                False,
                "NEOS",
                "An e-mail address is required for NEOS. Use --email <address>.",
            )
        client = NeosClient(url=neos_url, timeout=timeout)
        ok, out = client.submit_and_wait(lp_path, email)
        return (ok, "NEOS (CPLEX)", out)

    if solver_lower == "cplex":
        return _try_cplex()
    if solver_lower == "highs":
        return _try_highs()
    if solver_lower == "glpk":
        return _try_glpk()
    if solver_lower == "neos":
        return _try_neos()

    # auto: try local solvers in order, fall back to NEOS
    for label, fn in [("CPLEX", _try_cplex), ("HiGHS", _try_highs), ("GLPK", _try_glpk)]:
        ok, solver_name, out = fn()
        if ok:
            return (True, solver_name, out)
        tried.append((label, out))

    # Fall back to NEOS
    if email:
        return _try_neos()

    # No solver found and no email provided
    tried_msg = "\n".join(f"  {n}: {o}" for n, o in tried)
    return (
        False,
        "none",
        f"No local solver found and --email not provided for NEOS.\n{tried_msg}",
    )


# ---------------------------------------------------------------------------
# High-level analysis entry point
# ---------------------------------------------------------------------------


def check_lp(
    lp_path: Path,
    *,
    analyze_only: bool = False,
    solver: str = "auto",
    email: str = "",
    neos_url: str = _NEOS_DEFAULT_URL,
    timeout: int = 10,
    output_file: Optional[Path] = None,
) -> int:
    """
    Run the full infeasibility analysis pipeline on *lp_path*.

    Returns 0 on success, 1 on error.
    """
    if not lp_path.exists():
        print(_c(_RED, f"Error: file not found: {lp_path}"), file=sys.stderr)
        return 1

    report_parts: list[str] = []

    # 1 ── Static analysis ──────────────────────────────────────────────────
    print(f"\nAnalyzing: {_c(_BOLD, str(lp_path))}")
    print("  Running static analysis …")

    try:
        stats = analyze_lp_file(lp_path)
    except FileNotFoundError as exc:
        print(_c(_RED, f"Error: {exc}"), file=sys.stderr)
        return 1

    static_report = format_static_report(lp_path, stats)
    print(static_report)
    report_parts.append(static_report)

    if analyze_only:
        if output_file:
            _write_report(output_file, report_parts)
        return 0

    # 2 ── IIS / solver analysis ────────────────────────────────────────────
    available = _detect_local_solvers()
    if available:
        print(f"\n  Available local solvers: {', '.join(available)}")
    else:
        print("\n  No local solvers found on PATH.")

    if solver != "auto" or available or email:
        print(f"\n  Running IIS analysis (solver={solver}) …")
        result = run_iis(lp_path, solver, email, neos_url, timeout)
        if result is not None:
            ok, solver_name, out = result
            iis_report = _format_iis_report(solver_name, ok, out)
            print(iis_report)
            report_parts.append(iis_report)
        else:
            msg = "  (No IIS analysis performed.)"
            print(msg)
            report_parts.append(msg)
    else:
        hint = (
            f"\n{_c(_YELLOW, 'Tip:')} "
            "Install a local solver (CPLEX, HiGHS, or GLPK) or provide\n"
            "  --email <address> to submit to the NEOS server for IIS analysis."
        )
        print(hint)
        report_parts.append(hint)

    if output_file:
        _write_report(output_file, report_parts)

    return 0


def _format_iis_report(solver_name: str, success: bool, output: str) -> str:
    """Format the solver IIS output into a readable report section."""
    lines = [_header(f"IIS Analysis ({solver_name})")]
    if not success:
        lines.append(f"\n{_c(_RED, 'Solver error:')} {output}")
    else:
        lines.append(output)
    return "\n".join(lines)


def _write_report(output_file: Path, parts: list[str]) -> None:
    """Write the full report (with ANSI codes stripped) to *output_file*."""
    # Strip ANSI escape codes
    ansi_re = re.compile(r"\033\[[0-9;]*m")
    content = "\n\n".join(parts)
    content = ansi_re.sub("", content)
    try:
        output_file.write_text(content + "\n", encoding="utf-8")
        print(f"\nReport written to: {output_file}")
    except OSError as exc:
        print(f"Warning: could not write report: {exc}", file=sys.stderr)


