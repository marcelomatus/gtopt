def _add_options_sheet(ws: Any, planning_options: dict, styles: dict) -> None:
    """Write the options sheet with key|value|description columns."""
    ws.column_dimensions["A"].width = 34
    ws.column_dimensions["B"].width = 22
    ws.column_dimensions["C"].width = 50

    # Header row
    for col_idx, header in enumerate(["option", "value", "description"], start=1):
        c = ws.cell(row=1, column=col_idx, value=header)
        c.font = styles["HEADER_FONT"]
        c.fill = styles["HEADER_FILL"]

    pairs = _flatten_options(planning_options)
    for row_idx, (key, val) in enumerate(pairs, start=2):
        ws.cell(row=row_idx, column=1, value=key)
        ws.cell(row=row_idx, column=2, value=val)

    ws.freeze_panes = "A2"


def _add_data_sheet(
    ws: Any,
    sheet_name: str,
    records: list[dict],
    styles: dict,
    get_column_letter: Any,
) -> None:
    """Write a data sheet from a list of JSON records (no help rows)."""
    df = _records_to_df(records, sheet_name)
    if df.empty:
        return

    cols = list(df.columns)

    # Row 1: header
    for col_idx, col_name in enumerate(cols, start=1):
        c = ws.cell(row=1, column=col_idx, value=col_name)
        c.font = styles["HEADER_FONT"]
        c.fill = styles["HEADER_FILL"]

    # Row 2+: data
    for row_idx, row in enumerate(df.itertuples(index=False), start=2):
        for col_idx, val in enumerate(row, start=1):
            if val is None or (isinstance(val, float) and pd.isna(val)):
                continue
            ws.cell(row=row_idx, column=col_idx, value=val)

    # Column widths
    for col_idx, col_name in enumerate(cols, start=1):
        col_letter = get_column_letter(col_idx)
        ws.column_dimensions[col_letter].width = max(12, len(str(col_name)) + 4)

    ws.freeze_panes = "A2"


def _add_timeseries_sheet(
    ws: Any,
    sheet_name: str,
    df: pd.DataFrame,
    styles: dict,
    get_column_letter: Any,
) -> None:
    """Write a time-series @-sheet from a DataFrame."""
    if df.empty:
        return

    cols = list(df.columns)

    # Row 1: header
    for col_idx, col_name in enumerate(cols, start=1):
        c = ws.cell(row=1, column=col_idx, value=col_name)
        c.font = styles["HEADER_FONT"]
        c.fill = styles["HEADER_FILL"]

    # Row 2+: data (use itertuples for performance)
    for row_idx, row in enumerate(df.itertuples(index=False), start=2):
        for col_idx, val in enumerate(row, start=1):
            if val is None:
                continue
            # Handle numpy scalars
            try:
                import numpy as np

                if isinstance(val, (np.integer,)):
                    val = int(val)
                elif isinstance(val, (np.floating,)):
                    val = float(val)
                    if pd.isna(val):
                        continue
            except ImportError:
                pass
            ws.cell(row=row_idx, column=col_idx, value=val)

    # Column widths
    for col_idx, col_name in enumerate(cols, start=1):
        col_letter = get_column_letter(col_idx)
        ws.column_dimensions[col_letter].width = max(10, len(str(col_name)) + 3)

    ws.freeze_panes = "A2"


def build_plp_excel(
    planning: dict[str, Any],
    output_dir: Path,
    output_path: Path,
    options: dict[str, Any],
) -> None:
    """Build an igtopt-compatible Excel workbook from PLP planning data.

    The workbook contains:
    - ``.introduction`` sheet – overview and sheet guide
    - ``.plpinfo`` sheet – conversion statistics and metadata
    - ``options`` sheet – flattened planning options (key|value)
    - One sheet per non-empty array in simulation/system
    - Time-series ``CamelCase@fieldname`` sheets from Parquet files in output_dir

    Args:
        planning: Planning dict returned by ``GTOptWriter.to_json()``.
        output_dir: Directory containing Parquet/CSV time-series files.
        output_path: Destination ``.xlsx`` file path.
        options: Original plp2gtopt conversion options (for ``.plpinfo``).
    """
    try:
        import openpyxl
        from openpyxl.styles import Alignment, Font, PatternFill
        from openpyxl.utils import get_column_letter
    except ImportError as exc:
        raise ImportError(
            f"openpyxl is required for Excel output. "
            f"Install with: pip install openpyxl\n{exc}"
        ) from exc

    # -- Style definitions (consistent with igtopt template_builder) ----------
    styles: dict[str, Any] = {
        "TITLE": Font(name="Calibri", size=16, bold=True, color="1F3864"),
        "HEADING": Font(name="Calibri", size=12, bold=True, color="2F5496"),
        "BODY": Font(name="Calibri", size=10),
        "FOOTER": Font(name="Calibri", size=9, italic=True, color="808080"),
        "HEADER_FILL": PatternFill("solid", fgColor="1F3864"),
        "ALT_ROW": PatternFill("solid", fgColor="DCE6F1"),
        "TABLE_HEADER_FILL": PatternFill("solid", fgColor="4472C4"),
        "HEADER_FONT": Font(name="Calibri", bold=True, color="FFFFFF"),
        "TABLE_HEADER_FONT": Font(name="Calibri", bold=True, color="FFFFFF"),
        "ALIGN_LEFT": Alignment(horizontal="left"),
    }

    wb = openpyxl.Workbook()

    # 1. .introduction sheet (created as the default sheet)
    ws_intro = wb.active
    ws_intro.title = ".introduction"
    _add_intro_sheet(ws_intro, styles)

    # 2. .plpinfo sheet
    ws_info = wb.create_sheet(".plpinfo")
    _add_plpinfo_sheet(ws_info, planning, options, styles)

    # 3. options sheet
    ws_opts = wb.create_sheet("options")
    _add_options_sheet(ws_opts, planning.get("options", {}), styles)

    # 4. Simulation array sheets
    sim = planning.get("simulation", {})
    for sheet_name in _SIM_SHEETS:
        records = sim.get(sheet_name)
        if not records:
            continue
        ws = wb.create_sheet(sheet_name)
        _add_data_sheet(ws, sheet_name, records, styles, get_column_letter)

    # 5. System array sheets (skip non-array metadata like "name", "version")
    sys_data = planning.get("system", {})
    for sheet_name in _SYS_SHEETS:
        records = sys_data.get(sheet_name)
        if not records:
            continue
        ws = wb.create_sheet(sheet_name)
        _add_data_sheet(ws, sheet_name, records, styles, get_column_letter)

    # 6. Time-series @-sheets from Parquet files in output_dir.
    # Build uid→name maps from the planning dict so column names can be
    # written as human-readable element names (e.g. "LAJA") instead of
    # opaque "uid:N" keys.  igtopt resolves them back to uid:N when reading.
    uid_to_name_maps = _build_uid_to_name_maps(planning)

    if output_dir.exists():
        ts_count = 0
        for parquet_file in sorted(output_dir.rglob("*.parquet")):
            rel = parquet_file.relative_to(output_dir)
            parts = rel.parts
            # Expected: <CamelCaseDir>/<fieldname>.parquet
            if len(parts) != 2:
                _logger.debug("Skipping nested parquet: %s", parquet_file)
                continue
            cname = parts[0]
            fname = parquet_file.stem
            at_sheet_name = f"{cname}@{fname}"
            try:
                df = pd.read_parquet(parquet_file)
                if df.empty:
                    continue
                # Rename uid:N columns to element names if mapping available
                uid_to_name = uid_to_name_maps.get(cname, {})
                if uid_to_name:
                    df = df.rename(columns=uid_to_name)
                ws = wb.create_sheet(at_sheet_name)
                _add_timeseries_sheet(ws, at_sheet_name, df, styles, get_column_letter)
                ts_count += 1
                _logger.debug("Added @-sheet: %s (%d rows)", at_sheet_name, len(df))
            except Exception as exc:  # noqa: BLE001  # pylint: disable=broad-exception-caught
                # Intentionally broad: Parquet/Arrow/openpyxl can raise many
                # unrelated exception types (ArrowInvalid, OSError, ValueError,
                # UnsupportedOperation, etc.) and we must not crash the entire
                # Excel export just because one @-sheet's data is unreadable.
                _logger.warning("Could not read parquet file %s: %s", parquet_file, exc)

        _logger.info("Added %d time-series @-sheets from %s", ts_count, output_dir)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    wb.save(output_path)
    _logger.info("Excel workbook written: %s", output_path)
