"""
gtopt_check_lp — Analyze infeasible LP files generated by gtopt.

When gtopt generates an ``error*.lp`` file due to solver infeasibility, this
tool helps diagnose the cause by performing several complementary analyses:

1. **Static analysis** — parses the LP file directly and reports:
   - Variables with conflicting bounds (lb > ub)
   - Empty or fixed constraints
   - Very large or very small coefficients (numerical range issues)
   - Duplicate constraint names
   - Overall problem statistics

2. **Local IIS finder** — if a supported solver binary is on PATH, runs it to
   find the *Irreducible Infeasible Subsystem* (IIS).
   Supported solvers (tried in order):
   - CPLEX        (``cplex`` binary — uses ``refineconflict`` command)
   - HiGHS        (``highs`` binary or ``highspy`` Python package)
   - COIN-OR CLP  (``clp`` binary — same solver gtopt uses internally)
   - COIN-OR CBC  (``cbc`` binary)
   - GLPK         (``glpsol`` binary)

3. **NEOS server** — if no local solver is available, submits the LP to the
   NEOS online optimization server (https://neos-server.org) using CPLEX via
   its XML-RPC API, and waits for the conflict-analysis results.

Configuration
-------------
User preferences are stored in ``~/.gtopt_check_lp.conf`` (INI format).
On the first run the tool will prompt for these values interactively (when
stdin is a TTY) and suggest solver installation commands for any missing
open-source solvers.

Run ``gtopt_check_lp --init-config`` at any time to create or update the
configuration file.

Usage
-----
::

    gtopt_check_lp problem.lp
    gtopt_check_lp --last                       # auto-find newest error*.lp
    gtopt_check_lp problem.lp --analyze-only
    gtopt_check_lp problem.lp --solver coinor
    gtopt_check_lp problem.lp --use-neos --email user@example.com
    gtopt_check_lp problem.lp --output report.txt --verbose
    gtopt_check_lp --init-config                # create/update config file
"""

