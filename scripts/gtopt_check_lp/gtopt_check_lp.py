def check_lp(
    lp_path: Path,
    *,
    analyze_only: bool = False,
    solver: str = "all",
    email: str = "",
    neos_url: str = _NEOS_DEFAULT_URL,
    timeout: int = 5,
    output_file: Optional[Path] = None,
    ai: Optional[AiOptions] = None,
) -> int:
    """
    Run the full infeasibility analysis pipeline on *lp_path*.

    Parameters
    ----------
    lp_path:
        Path to the LP file to analyze.
    analyze_only:
        When True, perform only static analysis (no solver invocation).
    solver:
        Solver strategy: ``"all"``, ``"auto"``, ``"cplex"``, ``"highs"``,
        ``"coinor"``, ``"glpk"``, or ``"neos"``.
    email:
        E-mail address for NEOS submissions.
    neos_url:
        NEOS XML-RPC endpoint.
    timeout:
        Maximum seconds to wait for any local/NEOS solver (default 5).
    output_file:
        If set, write the full report to this file (ANSI codes stripped).
    ai:
        :class:`AiOptions` controlling the optional AI diagnostics step.
        ``None`` is equivalent to ``AiOptions()`` (enabled, Claude).

    Returns
    -------
    0 on success, 1 on fatal error.
    """
    ai_opts = ai if ai is not None else AiOptions()

    if not lp_path.exists():
        print(_c(_RED, f"Error: file not found: {lp_path}"), file=sys.stderr)
        return 1

    report_parts: list[str] = []

    # 1 ── Static analysis ────────────────────────────────────────────────────
    print(f"\nAnalyzing: {_c(_BOLD, str(lp_path))}")
    print("  Running static analysis …")

    try:
        stats = analyze_lp_file(lp_path)
    except FileNotFoundError as exc:
        print(_c(_RED, f"Error: {exc}"), file=sys.stderr)
        return 1

    static_report = format_static_report(lp_path, stats)
    print(static_report)
    report_parts.append(static_report)

    if analyze_only:
        if output_file:
            _write_report(output_file, report_parts)
        return 0

    # 2 ── IIS / solver analysis ──────────────────────────────────────────────
    available = detect_local_solvers()
    if available:
        print(f"\n  Available local solvers: {', '.join(available)}")
    else:
        print("\n  No local solvers found on PATH.")

    if solver != "auto" or available or email:
        print(f"\n  Running solver analysis (solver={solver}) …")
        ok, solver_name, out = run_iis(
            lp_path, solver=solver, email=email, neos_url=neos_url, timeout=timeout
        )
        iis_report = _format_iis_report(solver_name, ok, out)
        print(iis_report)
        report_parts.append(iis_report)
    else:
        hint = (
            f"\n{_c(_YELLOW, 'Tip:')} "
            "Install a local solver (CPLEX, HiGHS, CLP/CBC, or GLPK) or provide\n"
            "  --email <address> to submit to the NEOS server for IIS analysis.\n"
            "  CLP/CBC (coinor-clp / coinor-cbc) are usually available on\n"
            "  systems where gtopt is installed: sudo apt install coinor-clp coinor-cbc"
        )
        print(hint)
        report_parts.append(hint)

    # 3 ── AI diagnostics ─────────────────────────────────────────────────────
    if ai_opts.enabled:
        _run_ai_diagnostics(
            report_parts,
            provider=ai_opts.provider,
            model=ai_opts.model or None,
            prompt_template=ai_opts.prompt or _AI_INFEASIBILITY_PROMPT,
            api_key=ai_opts.key or None,
            timeout=ai_opts.timeout,
        )

    if output_file:
        _write_report(output_file, report_parts)

    return 0


def _run_ai_diagnostics(
    report_parts: list[str],
    *,
    provider: str,
    model: Optional[str],
    prompt_template: str,
    api_key: Optional[str],
    timeout: int,
) -> None:
    """Call the AI provider with the combined report and print the response."""
    full_report = "\n\n".join(report_parts)
    print(f"\n  Querying AI provider ({provider}) for diagnostics …")
    ok, response = query_ai(
        full_report,
        provider=provider,
        model=model,
        prompt_template=prompt_template,
        api_key=api_key,
        timeout=timeout,
    )
    ai_section = _header(f"AI Diagnostics ({provider})")
    if not ok:
        msg = f"\n{_c(_YELLOW, 'AI diagnostic unavailable:')} {response}"
        print(msg)
        report_parts.append(ai_section + msg)
    else:
        print(f"\n{ai_section}")
        print(response)
        report_parts.append(f"{ai_section}\n{response}")


def _format_iis_report(solver_name: str, success: bool, output: str) -> str:
    """Format the solver IIS output into a readable report section."""
    lines = [_header(f"IIS Analysis ({solver_name})")]
    if not success:
        lines.append(f"\n{_c(_RED, 'Solver error:')} {output}")
    else:
        lines.append(output)
    return "\n".join(lines)


def _write_report(output_file: Path, parts: list[str]) -> None:
    """Write the full report (with ANSI codes stripped) to *output_file*."""
    ansi_re = re.compile(r"\033\[[0-9;]*m")
    content = "\n\n".join(parts)
    content = ansi_re.sub("", content)
    try:
        output_file.write_text(content + "\n", encoding="utf-8")
        print(f"\nReport written to: {output_file}")
    except OSError as exc:
        print(f"Warning: could not write report: {exc}", file=sys.stderr)


