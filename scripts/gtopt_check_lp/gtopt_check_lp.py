def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="gtopt-check-lp",
        description=(
            "Analyze an infeasible LP file generated by gtopt.\n\n"
            "Performs static analysis (bound conflicts, empty constraints,\n"
            "numerical range) and optionally finds the IIS using a local\n"
            "solver (CPLEX, HiGHS, GLPK) or the NEOS online server."
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  gtopt-check-lp error_0.lp\n"
            "  gtopt-check-lp error_0.lp --analyze-only\n"
            "  gtopt-check-lp error_0.lp --solver highs\n"
            "  gtopt-check-lp error_0.lp --solver neos --email user@example.com\n"
            "  gtopt-check-lp error_0.lp --output report.txt\n"
        ),
    )

    parser.add_argument("lp_file", metavar="LP_FILE", help="Path to the LP file.")

    parser.add_argument(
        "--analyze-only",
        action="store_true",
        default=False,
        help="Perform only static analysis; do not invoke any solver.",
    )

    parser.add_argument(
        "--solver",
        default="auto",
        choices=["auto", "cplex", "highs", "glpk", "neos"],
        help=(
            "IIS solver to use.  'auto' tries local solvers first, "
            "then NEOS (default: auto)."
        ),
    )

    parser.add_argument(
        "--email",
        default="",
        metavar="EMAIL",
        help="E-mail address for NEOS server submissions (required for NEOS).",
    )

    parser.add_argument(
        "--solver-url",
        default=_NEOS_DEFAULT_URL,
        metavar="URL",
        help=(
            f"XML-RPC endpoint for a NEOS-compatible server "
            f"(default: {_NEOS_DEFAULT_URL})."
        ),
    )

    parser.add_argument(
        "--timeout",
        type=int,
        default=10,
        metavar="SECONDS",
        help="Maximum time to wait for a solver result (default: 10 s).",
    )

    parser.add_argument(
        "--output",
        default=None,
        metavar="FILE",
        help="Write the analysis report to FILE (ANSI codes stripped).",
    )

    parser.add_argument(
        "--no-color",
        action="store_true",
        default=False,
        help="Disable ANSI color codes in terminal output.",
    )

    parser.add_argument(
        "-v",
        "--verbose",
        action="store_true",
        default=False,
        help="Enable verbose / debug logging.",
    )

    parser.add_argument(
        "--version",
        action="version",
        version=f"%(prog)s {__version__}",
    )

    return parser


def main(argv: Optional[list[str]] = None) -> int:
    """CLI entry point.  Returns the exit code."""
    parser = _build_parser()
    args = parser.parse_args(argv)

    # Configure logging
    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.WARNING,
        format="%(levelname)s: %(message)s",
    )

    # Configure color output
    global _USE_COLOR  # noqa: PLW0603
    _USE_COLOR = not args.no_color and sys.stdout.isatty()

    lp_path = Path(args.lp_file)
    output_file = Path(args.output) if args.output else None

    return check_lp(
        lp_path,
        analyze_only=args.analyze_only,
        solver=args.solver,
        email=args.email,
        neos_url=args.solver_url,
        timeout=args.timeout,
        output_file=output_file,
    )


if __name__ == "__main__":
    sys.exit(main())
