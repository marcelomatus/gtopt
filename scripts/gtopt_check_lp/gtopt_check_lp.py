def format_static_report(lp_path: Path, stats: LPStats) -> str:
    """Return a formatted analysis report string."""
    lines = []
    lines.append(_header(f"Static Analysis: {lp_path.name}"))

    # Problem statistics
    lines.append(f"\n{_c(_BOLD, 'Problem statistics')}")
    sense = "Minimization" if stats.is_minimization else "Maximization"
    lines.append(f"  Objective:      {sense}")
    lines.append(f"  Variables:      {stats.n_vars}")
    lines.append(f"  Constraints:    {stats.n_constraints}")
    if stats.n_integers:
        lines.append(f"  Integer vars:   {stats.n_integers}")
    if stats.n_binary:
        lines.append(f"  Binary vars:    {stats.n_binary}")
    if stats.max_abs_coeff > 0:
        lines.append(
            f"  Max |coeff|:    {stats.max_abs_coeff:.3e}"
            + (
                _c(_RED, "  ← very large")
                if stats.max_abs_coeff >= _LARGE_COEFF_THRESHOLD
                else ""
            )
        )
    if stats.min_abs_nonzero_coeff < float("inf") and stats.min_abs_nonzero_coeff > 0:
        lines.append(
            f"  Min |coeff| ≠0: {stats.min_abs_nonzero_coeff:.3e}"
            + (
                _c(_YELLOW, "  ← very small")
                if stats.min_abs_nonzero_coeff <= _SMALL_COEFF_THRESHOLD
                else ""
            )
        )
    coeff_ratio = (
        stats.max_abs_coeff / stats.min_abs_nonzero_coeff
        if stats.min_abs_nonzero_coeff > 0 and stats.max_abs_coeff > 0
        else 0.0
    )
    if coeff_ratio > 1e6:
        lines.append(
            f"  Coeff ratio:    {coeff_ratio:.3e}"
            + _c(_YELLOW, "  ← poor numerical conditioning")
        )

    # Issues
    if not stats.has_issues():
        lines.append(
            f"\n{_c(_GREEN, '✓ No obvious static infeasibilities detected.')}"
        )
    else:
        lines.append(
            f"\n{_c(_BOLD, _c(_RED, '⚠ Potential infeasibility causes:'))}"
        )

        if stats.infeasible_bounds:
            lines.append(
                f"\n  {_c(_RED, f'Conflicting variable bounds ({len(stats.infeasible_bounds)}):')} "
                f"(lb > ub — variable domain is empty)"
            )
            for vb in stats.infeasible_bounds[:20]:
                lines.append(f"    • {vb.name}: lb={vb.lb:g}  >  ub={vb.ub:g}")
            if len(stats.infeasible_bounds) > 20:
                lines.append(
                    f"    … and {len(stats.infeasible_bounds) - 20} more"
                )

        if stats.empty_constraints:
            lines.append(
                f"\n  {_c(_YELLOW, f'Empty constraints ({len(stats.empty_constraints)}):')} "
                f"(no non-zero coefficients)"
            )
            for name in stats.empty_constraints[:10]:
                lines.append(f"    • {name}")
            if len(stats.empty_constraints) > 10:
                lines.append(
                    f"    … and {len(stats.empty_constraints) - 10} more"
                )

        if stats.duplicate_constraint_names:
            lines.append(
                f"\n  {_c(_YELLOW, f'Duplicate constraint names ({len(stats.duplicate_constraint_names)}):')} "
            )
            for name in stats.duplicate_constraint_names[:10]:
                lines.append(f"    • {name}")

        if stats.large_coeff_constraints:
            lines.append(
                f"\n  {_c(_YELLOW, f'Constraints with very large coefficients (≥ {_LARGE_COEFF_THRESHOLD:.0e}):')} "
                f"({len(stats.large_coeff_constraints)} constraints)"
            )
            for name in stats.large_coeff_constraints[:10]:
                lines.append(f"    • {name}")
            if len(stats.large_coeff_constraints) > 10:
                lines.append(
                    f"    … and {len(stats.large_coeff_constraints) - 10} more"
                )

    if stats.fixed_variables:
        lines.append(
            f"\n  {_c(_CYAN, f'Fixed variables ({len(stats.fixed_variables)}):')} (lb == ub)"
        )
        for name in stats.fixed_variables[:10]:
            lines.append(f"    • {name}")
        if len(stats.fixed_variables) > 10:
            lines.append(f"    … and {len(stats.fixed_variables) - 10} more")

    return "\n".join(lines)


