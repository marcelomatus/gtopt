_USE_COLOR = True  # written by main() before any output


def _c(code: str, text: str) -> str:
    return f"{code}{text}{_RESET}" if _USE_COLOR else text


def _header(title: str) -> str:
    return _col.header(title)


# ---------------------------------------------------------------------------
# AI diagnostics helper
# ---------------------------------------------------------------------------


def _run_ai_diagnostics(
    report_parts: list[str],
    *,
    provider: str,
    model: Optional[str],
    prompt_template: str,
    api_key: Optional[str],
    timeout: int,
) -> None:
    """Call the AI provider with the combined report and print the response."""
    full_report = "\n\n".join(report_parts)
    print(f"\n  Querying AI provider ({provider}) for diagnostics \u2026")
    ok, response = query_ai(
        full_report,
        provider=provider,
        model=model,
        prompt_template=prompt_template,
        api_key=api_key,
        timeout=timeout,
    )
    ai_section = _header(f"AI Diagnostics ({provider})")
    if not ok:
        msg = f"\n{_c(_YELLOW, 'AI diagnostic unavailable:')} {response}"
        print(msg)
        report_parts.append(ai_section + msg)
    else:
        print(f"\n{ai_section}")
        print(response)
        report_parts.append(f"{ai_section}\n{response}")


