def _collect_field_order(sheet_name: str) -> list[str]:
    """Return the preferred column order for a given sheet, from FIELD_META.

    Falls back to an empty list (caller should derive order from the data).
    """
    try:
        from igtopt.template_builder import FIELD_META  # pylint: disable=import-outside-toplevel

        return [f for f, *_ in FIELD_META.get(sheet_name, [])]
    except ImportError:
        return []


def _records_to_df(records: list[dict], sheet_name: str) -> pd.DataFrame:
    """Convert a list of JSON records to a DataFrame with canonical column order."""
    if not records:
        return pd.DataFrame()

    preferred = _collect_field_order(sheet_name)
    all_keys: list[str] = []
    seen: set[str] = set()
    # First add preferred keys that appear in any record
    for key in preferred:
        if key not in seen:
            all_keys.append(key)
            seen.add(key)
    # Then add any remaining keys from the records
    for rec in records:
        for key in rec:
            if key not in seen:
                all_keys.append(key)
                seen.add(key)

    rows: list[dict] = []
    for rec in records:
        row: dict = {}
        for key in all_keys:
            val = rec.get(key)
            if val is None:
                continue
            # Serialize complex values (lists, dicts) as JSON strings
            if isinstance(val, (list, dict)):
                import json

                val = json.dumps(val, separators=(",", ":"))
            row[key] = val
        rows.append(row)

    return pd.DataFrame(rows, columns=all_keys)


def _flatten_options(planning_options: dict) -> list[tuple[str, Any]]:
    """Flatten the planning options dict to (key, value) pairs for the options sheet.

    Nested dicts (like ``sddp_options``) are flattened with their sub-keys.
    Deeply nested dicts (e.g. ``cascade_options.levels[i].model_options``)
    are JSON-stringified so Excel can render them in one cell.
    """
    import json as _json  # noqa: PLC0415

    pairs: list[tuple[str, Any]] = []
    for key, val in planning_options.items():
        if isinstance(val, dict):
            for sub_key, sub_val in val.items():
                if sub_val is None:
                    continue
                if isinstance(sub_val, (dict, list)):
                    pairs.append((sub_key, _json.dumps(sub_val)))
                else:
                    pairs.append((sub_key, sub_val))
        elif isinstance(val, list):
            pairs.append((key, _json.dumps(val)))
        elif val is not None:
            pairs.append((key, val))
    return pairs


def _add_intro_sheet(ws: Any, styles: dict) -> None:
    """Write the .introduction sheet content."""
    ws.column_dimensions["A"].width = 30
    ws.column_dimensions["B"].width = 14
    ws.column_dimensions["C"].width = 58

    row = 1
    for entry in _INTRO_LINES:
        kind = entry[-1]
        if kind == "TITLE":
            cell = ws.cell(row=row, column=1, value=entry[0])
            cell.font = styles["TITLE"]
            ws.merge_cells(f"A{row}:C{row}")
        elif kind == "HEADING":
            cell = ws.cell(row=row, column=1, value=entry[0])
            cell.font = styles["HEADING"]
            ws.merge_cells(f"A{row}:C{row}")
        elif kind == "BODY":
            cell = ws.cell(row=row, column=1, value=entry[0])
            cell.font = styles["BODY"]
            ws.merge_cells(f"A{row}:C{row}")
        elif kind == "FOOTER":
            cell = ws.cell(row=row, column=1, value=entry[0])
            cell.font = styles["FOOTER"]
            ws.merge_cells(f"A{row}:C{row}")
        elif kind == "TABLE_HEADER":
            for col_idx, val in enumerate(entry[:3], start=1):
                c = ws.cell(row=row, column=col_idx, value=val)
                c.font = styles["TABLE_HEADER_FONT"]
                c.fill = styles["TABLE_HEADER_FILL"]
        elif kind == "TABLE":
            for col_idx, val in enumerate(entry[:3], start=1):
                c = ws.cell(row=row, column=col_idx, value=val)
                c.font = styles["BODY"]
                if row % 2 == 0:
                    c.fill = styles["ALT_ROW"]
        row += 1


