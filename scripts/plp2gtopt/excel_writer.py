"""Excel writer for converting PLP planning data to igtopt-compatible Excel workbook.

Produces a .xlsx workbook that can be converted back to gtopt JSON + Parquet
by running:  ``igtopt <workbook>.xlsx``

The workbook contains:
- ``.introduction`` sheet – human-readable description of the workbook
- ``.plpinfo`` sheet – PLP conversion statistics (element counts, dates, options)
- ``options`` sheet – gtopt solver / I/O options (key | value pairs)
- One data sheet per array in ``simulation`` and ``system``
- ``CamelCase@fieldname`` time-series sheets from Parquet files in the output dir
"""

from __future__ import annotations

import datetime
import logging
import re
from pathlib import Path
from typing import Any

import pandas as pd

_logger = logging.getLogger(__name__)


def _camel_to_snake_array(cname: str) -> str:
    """Convert a CamelCase element type name to its snake_case_array sheet name.

    Examples
    --------
    >>> _camel_to_snake_array("Demand")
    'demand_array'
    >>> _camel_to_snake_array("GeneratorProfile")
    'generator_profile_array'
    """
    snake = re.sub(r"([A-Z])", lambda m: "_" + m.group(1).lower(), cname)
    return snake.lstrip("_") + "_array"


def _build_uid_to_name_maps(planning: dict[str, Any]) -> dict[str, dict[str, str]]:
    """Build ``uid:N`` → element-name maps for all arrays in the planning dict.

    For each array (in both ``simulation`` and ``system`` sections), build a
    mapping from ``uid:N`` column keys to the human-readable element name.
    This is used when writing time-series ``@``-sheets to the Excel workbook,
    so that output columns use the element name (e.g. ``"LAJA"``) instead of
    the opaque ``uid:N`` key.

    Only arrays where every element has unique names are included; if any two
    elements share a name the whole type is silently omitted to avoid
    ambiguity.

    Args:
        planning: The planning dict returned by ``GTOptWriter.to_json()``.

    Returns:
        Dict mapping CamelCase type names to ``{uid_col: element_name}`` maps.
        E.g. ``{"Demand": {"uid:1": "LAJA", "uid:2": "ANTUCO"}, ...}``.
    """
    result: dict[str, dict[str, str]] = {}
    for section in ("simulation", "system"):
        for array_name, records in planning.get(section, {}).items():
            if not isinstance(records, list) or not records:
                continue
            # Derive CamelCase type name from snake_case array name
            if array_name.endswith("_array"):
                base = array_name[:-6]
            else:
                continue
            cname = re.sub(r"_([a-z])", lambda m: m.group(1).upper(), base)
            cname = cname[0].upper() + cname[1:] if cname else cname

            uid_to_name: dict[str, str] = {}
            seen_names: set[str] = set()
            ok = True
            for rec in records:
                uid = rec.get("uid")
                name = rec.get("name")
                if uid is None or name is None:
                    ok = False
                    break
                name_str = str(name)
                if name_str in seen_names:
                    # Duplicate name – skip this type entirely
                    ok = False
                    break
                seen_names.add(name_str)
                uid_to_name[f"uid:{uid}"] = name_str
            if ok and uid_to_name:
                result[cname] = uid_to_name
    return result


