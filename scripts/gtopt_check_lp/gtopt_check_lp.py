# Local GLPK IIS helper
# ---------------------------------------------------------------------------


def run_local_glpk(lp_path: Path, timeout: int = 120) -> tuple[bool, str]:
    """
    Run ``glpsol`` (GLPK) to attempt LP analysis.

    GLPK does not support IIS finding directly, but the output helps confirm
    the infeasibility and may hint at which constraints are involved via
    sensitivity analysis.

    Returns ``(success, output)``.
    """
    glpsol_bin = shutil.which("glpsol")
    if glpsol_bin is None:
        return False, "glpsol binary not found on PATH."

    log.debug("Running GLPK from: %s", glpsol_bin)
    try:
        result = subprocess.run(
            [glpsol_bin, "--lp", str(lp_path), "--ranges", "/dev/null"],
            capture_output=True,
            text=True,
            timeout=timeout,
            check=False,
        )
        return True, result.stdout + result.stderr
    except subprocess.TimeoutExpired:
        return False, f"glpsol timed out after {timeout}s."
    except OSError as exc:
        return False, f"Failed to run glpsol: {exc}"


# ---------------------------------------------------------------------------
# NEOS XML-RPC client
# ---------------------------------------------------------------------------

_NEOS_DEFAULT_URL = "https://neos-server.org:3333"

_NEOS_LP_CPLEX_XML = """\
<document>
<category>lp</category>
<solver>CPLEX</solver>
<inputMethod>LP</inputMethod>
<client>gtopt-check-lp</client>
<email>{email}</email>
<LP><![CDATA[
{lp_content}
]]></LP>
<commands><![CDATA[
conflict
display conflict all
]]></commands>
<comment><![CDATA[
LP infeasibility analysis submitted by gtopt-check-lp {version}
]]></comment>
</document>
"""


class NeosClient:
    """
    Client for the NEOS optimization server XML-RPC API.

    See https://neos-server.org/neos/xml-rpc.html for the API reference.
    """

    def __init__(self, url: str = _NEOS_DEFAULT_URL, timeout: int = 10) -> None:
        self.url = url
        self.timeout = timeout
        self._proxy: Optional[xmlrpc.client.ServerProxy] = None

    def _get_proxy(self) -> xmlrpc.client.ServerProxy:
        if self._proxy is None:
            self._proxy = xmlrpc.client.ServerProxy(self.url)
        return self._proxy

    def ping(self) -> bool:
        """Return True if the NEOS server is reachable."""
        try:
            msg = self._get_proxy().ping()
            return "NEOS" in str(msg)
        except Exception:  # noqa: BLE001
            return False

    def submit_lp(
        self, lp_path: Path, email: str
    ) -> tuple[Optional[int], Optional[str]]:
        """
        Submit an LP file to NEOS.

        Returns ``(job_number, password)`` or ``(None, error_message)``.
        """
        lp_content = lp_path.read_text(encoding="utf-8", errors="replace")
        xml = _NEOS_LP_CPLEX_XML.format(
            email=email,
            lp_content=lp_content,
            version=__version__,
        )
        try:
            proxy = self._get_proxy()
            result = proxy.submitJob(xml)
            if isinstance(result, (list, tuple)) and len(result) == 2:
                job_number, password = result
                if isinstance(job_number, int) and job_number > 0:
                    return job_number, password
                return None, f"NEOS returned error: {result}"
            return None, f"Unexpected NEOS response: {result}"
        except xmlrpc.client.Fault as exc:
            return None, f"NEOS XML-RPC fault: {exc}"
        except OSError as exc:
            return None, f"Network error connecting to {self.url}: {exc}"

    def wait_for_result(
        self,
        job_number: int,
        password: str,
        poll_interval: float = 5.0,
    ) -> tuple[bool, str]:
        """
        Poll until the NEOS job completes and return ``(success, output)``.
        """
        proxy = self._get_proxy()
        deadline = time.monotonic() + self.timeout
        last_status = ""

        while time.monotonic() < deadline:
            try:
                status = str(proxy.getJobStatus(job_number, password))
            except Exception as exc:  # noqa: BLE001
                return False, f"Error polling NEOS: {exc}"

            if status != last_status:
                log.info("NEOS job %d: %s", job_number, status)
                last_status = status

            if status == "Done":
                try:
                    raw = proxy.getFinalResults(job_number, password)
                    # getFinalResults may return bytes or a Binary object
                    if hasattr(raw, "data"):
                        output = raw.data.decode("utf-8", errors="replace")
                    elif isinstance(raw, bytes):
                        output = raw.decode("utf-8", errors="replace")
                    else:
                        output = str(raw)
                    return True, output
                except Exception as exc:  # noqa: BLE001
                    return False, f"Error retrieving NEOS results: {exc}"

            if status in ("Error", "Unknown"):
                return False, f"NEOS job {job_number} ended with status: {status}"

            time.sleep(poll_interval)

        return False, f"Timed out waiting for NEOS job {job_number} after {self.timeout}s."

    def submit_and_wait(
        self, lp_path: Path, email: str, poll_interval: float = 5.0
    ) -> tuple[bool, str]:
        """
        Submit an LP to NEOS and wait for the result.

        Returns ``(success, output)``.
        """
        if not self.ping():
            return (
                False,
                f"Cannot reach NEOS server at {self.url}. "
                "Check your internet connection.",
            )

        print(f"  Submitting to NEOS ({self.url}) …")
        job_number, password_or_err = self.submit_lp(lp_path, email)
        if job_number is None:
            return False, str(password_or_err)

        print(f"  NEOS job {job_number} submitted.  Waiting for results …")
        return self.wait_for_result(
            job_number, str(password_or_err), poll_interval=poll_interval
        )


