def main(argv: Optional[list[str]] = None) -> int:
    """CLI entry point.  Returns the exit code."""
    parser = _build_parser()
    args = parser.parse_args(argv)

    # ── Config file path ──────────────────────────────────────────────────────
    config_path = Path(args.config) if args.config else _default_config_path()

    # ── Logging ───────────────────────────────────────────────────────────────
    log_level = args.log_level or ("DEBUG" if args.verbose else "WARNING")
    logging.basicConfig(
        level=getattr(logging, log_level),
        format="%(levelname)s: %(message)s",
    )

    # ── --init-config: run wizard and exit ────────────────────────────────────
    if args.init_config:
        global _USE_COLOR  # noqa: PLW0603
        _USE_COLOR = not args.no_color and sys.stdout.isatty()
        _col.USE_COLOR = _USE_COLOR
        run_interactive_setup(config_path, use_color=_USE_COLOR)
        return 0

    # ── Load config ───────────────────────────────────────────────────────────
    config_exists = config_path.exists()
    cfg = load_config(config_path)

    # ── Color (resolve before any output) ─────────────────────────────────────
    color_setting = cfg.get("color", "auto")
    if args.no_color or color_setting == "never":
        _USE_COLOR = False
    elif color_setting == "always":
        _USE_COLOR = True
    else:
        _USE_COLOR = sys.stdout.isatty()
    _col.USE_COLOR = _USE_COLOR

    # ── First-run setup ───────────────────────────────────────────────────────
    # Quiet mode always skips interactive setup (it must never block).
    skip_setup = args.no_setup or args.quiet
    if not config_exists and not skip_setup and sys.stdin.isatty():
        print(_c(_YELLOW, f"No config file found at {config_path}."))
        print("Running first-time setup…")
        cfg = run_interactive_setup(config_path, use_color=_USE_COLOR)
    elif not config_exists and args.quiet:
        print(
            _c(
                _YELLOW,
                f"Warning: no config file at {config_path}; "
                "using defaults (quiet mode).",
            ),
            file=sys.stderr,
        )

    # ── --show-config ─────────────────────────────────────────────────────────
    if args.show_config:
        print(_c(_BOLD, f"\nActive configuration ({config_path}):"))
        for key, val in cfg.items():
            print(f"  {key} = {val!r}")
        print_solver_status(use_color=_USE_COLOR)
        return 0

    # ── Merge CLI overrides into effective config ──────────────────────────────
    effective_email = args.email if args.email is not None else cfg.get("email", "")
    effective_solver = (
        args.solver if args.solver is not None else cfg.get("solver", "auto")
    )
    effective_timeout = (
        args.timeout if args.timeout is not None else int(cfg.get("timeout", "5"))
    )
    effective_neos_url = (
        args.solver_url
        if args.solver_url is not None
        else cfg.get("neos_url", _NEOS_DEFAULT_URL)
    )

    # ── AI settings ───────────────────────────────────────────────────────────
    cfg_ai_enabled = cfg.get("ai_enabled", "true").lower() not in ("false", "0", "no")
    effective_ai_enabled = (
        args.ai_enabled if args.ai_enabled is not None else cfg_ai_enabled
    )
    effective_ai_provider = (
        args.ai_provider
        if args.ai_provider is not None
        else cfg.get("ai_provider", _AI_DEFAULT_PROVIDER)
    )
    effective_ai_model = (
        args.ai_model if args.ai_model is not None else cfg.get("ai_model", "")
    )
    effective_ai_prompt = (
        args.ai_prompt if args.ai_prompt is not None else cfg.get("ai_prompt", "")
    )
    effective_ai_key = args.ai_key if args.ai_key is not None else ""

    # ── Solver startup message ────────────────────────────────────────────────
    available = detect_local_solvers()
    config_source = str(config_path) if config_exists else "defaults"
    if not args.quiet:
        solvers_str = ", ".join(available) if available else "none"
        print(
            f"Solver: {effective_solver} "
            f"(from {config_source})  "
            f"[available: {solvers_str}]"
        )

    # ── Resolve LP file ───────────────────────────────────────────────────────
    if args.last:
        lp_path = _find_latest_error_lp()
        if lp_path is None:
            if args.quiet:
                print(
                    _c(
                        _YELLOW,
                        "Warning: --last specified but no error*.lp file found "
                        "(quiet mode).",
                    ),
                    file=sys.stderr,
                )
                return 0
            print(
                _c(_RED, "Error: --last specified but no error*.lp file found."),
                file=sys.stderr,
            )
            return 1
        print(f"Auto-selected LP file: {_c(_BOLD, str(lp_path))}")
    elif args.lp_file is not None:
        lp_path = Path(args.lp_file)
    else:
        parser.print_help()
        return 1

    output_file = Path(args.output) if args.output else None

    # ── Benchmark mode ────────────────────────────────────────────────────────
    if args.benchmark:
        print(f"\nBenchmarking: {_c(_BOLD, str(lp_path))}")
        report = run_benchmark(
            lp_path,
            timeout=effective_timeout,
        )
        table = format_benchmark_table(report, use_color=_USE_COLOR)
        print(table)
        if output_file:
            _write_report(output_file, [table])
        return 0

    return check_lp(
        lp_path,
        analyze_only=args.analyze_only,
        solver=effective_solver,
        algo=args.algo or "",
        optimal_eps=args.optimal_eps or 0.0,
        feasible_eps=args.feasible_eps or 0.0,
        barrier_eps=args.barrier_eps or 0.0,
        email=effective_email,
        neos_url=effective_neos_url,
        timeout=effective_timeout,
        output_file=output_file,
        quiet=args.quiet,
        no_neos=args.no_neos,
        ai=AiOptions(
            enabled=effective_ai_enabled,
            provider=effective_ai_provider,
            model=effective_ai_model,
            prompt=effective_ai_prompt,
            key=effective_ai_key,
        ),
    )


if __name__ == "__main__":
    sys.exit(main())
