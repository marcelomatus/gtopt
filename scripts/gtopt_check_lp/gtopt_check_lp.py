"""
gtopt_check_lp -- Analyze infeasible LP files generated by gtopt.

When gtopt generates an ``error*.lp`` file due to solver infeasibility, this
tool helps diagnose the cause by performing several complementary analyses:

1. **Static analysis** -- parses the LP file directly and reports:
   - Variables with conflicting bounds (lb > ub)
   - Empty or fixed constraints
   - Very large or very small coefficients (numerical range issues)
   - Duplicate constraint names
   - Overall problem statistics

2. **Local IIS finder** -- if a supported solver binary is on PATH, runs it to
   find the *Irreducible Infeasible Subsystem* (IIS).
   Supported solvers (tried in order):
   - CPLEX        (``cplex`` binary -- uses ``tools conflict`` command)
   - HiGHS        (``highs`` binary or ``highspy`` Python package)
   - COIN-OR CLP  (``clp`` binary -- same solver gtopt uses internally)
   - COIN-OR CBC  (``cbc`` binary)

3. **NEOS server** -- if no local solver is available and an e-mail is
   configured, submits the LP to the NEOS online optimization server
   (https://neos-server.org) using CPLEX via its XML-RPC API, and waits
   for the conflict-analysis results.

Configuration
-------------
User preferences are stored in the unified ``~/.gtopt.conf`` (INI format).
AI settings live in the ``[global]`` section (shared with other gtopt tools);
tool-specific settings (email, solver, timeout) live in the
``[gtopt_check_lp]`` section.

On the first run the tool will prompt for these values interactively (when
stdin is a TTY) and suggest solver installation commands for any missing
open-source solvers.

Run ``gtopt_check_lp --init-config`` at any time to create or update the
configuration file.

**Migration**: if ``~/.gtopt_check_lp.conf`` exists and ``~/.gtopt.conf``
does not, settings are migrated automatically on first use.

Quiet mode (``--quiet``)
------------------------
In quiet mode the tool:

* **Never fails** -- always exits with code 0.
* **Never prompts** -- skips the interactive first-run setup wizard even when
  no config file is present; prints a warning instead.
* **Tries every available solver** -- uses all local solvers, then falls back
  to NEOS if an e-mail is configured.  If neither is available it prints a
  warning with installation hints.
* **Handles NEOS gracefully** -- if NEOS is unreachable or returns an error,
  prints a warning and continues with the static-analysis results.
* **Handles AI gracefully** -- if the AI provider is not configured or the
  call fails, prints a warning and continues.

Quiet mode is used by the ``gtopt`` binary to run the diagnostic after an
infeasibility event without risking a stall or a non-zero exit code.

Usage
-----
::

    gtopt_check_lp problem.lp
    gtopt_check_lp problem.lp.gz             # gzip-compressed LP file
    gtopt_check_lp problem.lp.gzip           # alternative gzip extension
    gtopt_check_lp --last                       # auto-find newest error*.lp[.gz]
    gtopt_check_lp problem.lp --analyze-only
    gtopt_check_lp problem.lp --quiet           # non-failing, used by gtopt
    gtopt_check_lp problem.lp --solver coinor
    gtopt_check_lp problem.lp --no-neos --solver coinor
    gtopt_check_lp problem.lp --email user@example.com
    gtopt_check_lp problem.lp --output report.txt --verbose
    gtopt_check_lp --init-config                # create/update config file
"""

