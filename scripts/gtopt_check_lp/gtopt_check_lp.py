def check_lp(  # pylint: disable=too-many-arguments
    lp_path: Path,
    *,
    analyze_only: bool = False,
    solver: str = "all",
    algo: str = "",
    optimal_eps: float = 0.0,
    feasible_eps: float = 0.0,
    barrier_eps: float = 0.0,
    email: str = "",
    neos_url: str = _NEOS_DEFAULT_URL,
    timeout: int = 5,
    output_file: Optional[Path] = None,
    ai: Optional[AiOptions] = None,
    quiet: bool = False,
    no_neos: bool = False,
    full: bool = False,
) -> int:
    """
    Run the full infeasibility analysis pipeline on *lp_path*.

    Parameters
    ----------
    lp_path:
        Path to the LP file to analyze.  Plain ``.lp`` files and
        gzip-compressed ``.lp.gz`` / ``.lp.gzip`` files are both accepted.
        When the exact path does not exist, the function automatically tries
        ``lp_path + ".gz"`` and ``lp_path + ".gzip"`` as fallbacks.
    analyze_only:
        When True, perform only static analysis (no solver invocation).
    solver:
        Solver strategy: ``"all"``, ``"auto"``, ``"cplex"``, ``"highs"``,
        ``"coinor"``, or ``"neos"``.
    algo:
        LP algorithm to use for COIN-OR and CPLEX solvers.  One of
        ``"default"``, ``"primal"``, ``"dual"``, ``"barrier"``, or ``""``
        (empty string means use the solver-specific default, which is
        ``"barrier"`` for COIN-OR and CPLEX).
    optimal_eps:
        Optimality (dual) tolerance for COIN-OR and CPLEX.  0.0 means use
        the solver's built-in default.  Passed by the gtopt binary to match
        the tolerance settings used in the original solve.
    feasible_eps:
        Feasibility (primal) tolerance for COIN-OR and CPLEX.  0.0 means use
        the solver's built-in default.
    barrier_eps:
        Barrier convergence tolerance for COIN-OR and CPLEX.  0.0 means use
        the solver's built-in default.
    email:
        E-mail address for NEOS submissions.
    neos_url:
        NEOS XML-RPC endpoint.
    timeout:
        Maximum seconds to wait for any local/NEOS solver (default 5).
    output_file:
        If set, write the full report to this file (ANSI codes stripped).
    ai:
        :class:`AiOptions` controlling the optional AI diagnostics step.
        ``None`` is equivalent to ``AiOptions()`` (enabled, Claude).
    quiet:
        When True, the function **never fails**: it always returns 0.
        In quiet mode:

        * Missing or unreadable LP files (including decompression errors)
          produce a warning instead of an error, and the function returns 0.
        * The solver analysis step always runs (even when ``solver="auto"``
          and no local solvers are available).
        * If local solvers are unavailable and *email* is configured, NEOS
          is tried automatically.  NEOS failures (unreachable, timeout, or
          missing e-mail) produce a warning rather than an error.
        * AI failures produce a warning rather than an exception.
        * Static-analysis exceptions produce a warning and return 0.
    no_neos:
        When True, skip NEOS submissions even if an e-mail is configured.

    Returns
    -------
    0 on success (or always in quiet mode), 1 on fatal error.
    """
    ai_opts = ai if ai is not None else AiOptions()

    # Suppress NEOS by clearing the email when requested.  Using a local
    # variable avoids shadowing the caller's `email` parameter.
    filtered_email = "" if no_neos else email

    # Resolve LP path: if the given path doesn't exist, try .gz / .gzip
    # compressed variants automatically.
    resolved = resolve_lp_path(lp_path)
    if resolved is None:
        if quiet:
            print(
                _c(_YELLOW, f"Warning: LP file not found: {lp_path}"),
                file=sys.stderr,
            )
            return 0
        print(_c(_RED, f"Error: file not found: {lp_path}"), file=sys.stderr)
        return 1

    # Tell the user when we resolved to a different (compressed) path.
    if resolved != lp_path:
        print(
            _c(_YELLOW, f"Note: {lp_path} not found; using {resolved}"),
        )
    lp_path = resolved

    # Validate that the resolved path is actually readable.
    if not lp_path.exists():
        if quiet:
            print(
                _c(_YELLOW, f"Warning: LP file not found: {lp_path}"),
                file=sys.stderr,
            )
            return 0
        print(_c(_RED, f"Error: file not found: {lp_path}"), file=sys.stderr)
        return 1

    report_parts: list[str] = []

    # Show a note when the file is compressed.
    compression_note = ""
    if is_compressed(lp_path):
        compression_note = _c(_CYAN, " [compressed]")

    # 1 -- Static analysis ────────────────────────────────────��───────────────
    print(f"\nAnalyzing: {_c(_BOLD, str(lp_path))}{compression_note}")
    print("  Running static analysis \u2026")

    stats = None
    try:
        stats = analyze_lp_file(lp_path)
    except Exception as exc:  # noqa: BLE001  # pylint: disable=broad-exception-caught
        # `except Exception` does NOT catch KeyboardInterrupt/SystemExit.
        if quiet:
            print(
                _c(_YELLOW, f"Warning: static analysis failed: {exc}"),
                file=sys.stderr,
            )
            # Continue to IIS / solver analysis instead of returning early.
        else:
            print(_c(_RED, f"Error: {exc}"), file=sys.stderr)
            return 1

    if stats is not None:
        static_report = format_static_report(lp_path, stats)
        print(static_report)
        report_parts.append(static_report)

    # Flush so the parent process receives output even if we are killed by
    # an external timeout wrapper (e.g. the gtopt binary's spawn_tool).
    sys.stdout.flush()

    if analyze_only:
        if output_file:
            _write_report(output_file, report_parts)
        return 0

    # 2 -- IIS / solver analysis ───��──────────────────────────────────────────
    available = detect_local_solvers()
    if available:
        print(f"\n  Available local solvers: {', '.join(available)}")
    else:
        print("\n  No local solvers found on PATH.")

    # Determine whether to run the solver step.
    # Quiet mode always runs it so the user gets whatever analysis is possible.
    run_solver_step = quiet or solver != "auto" or available or filtered_email

    if run_solver_step:
        print(f"\n  Running solver analysis (solver={solver}) \u2026")

        # In quiet mode, catch any unexpected exception from the solver layer.
        # Note: `except Exception` does NOT catch KeyboardInterrupt/SystemExit
        # (they are BaseException subclasses), so Ctrl-C still terminates the
        # process even in quiet mode.
        try:
            ok, solver_name, out = run_iis(
                lp_path,
                solver=solver,
                algo=algo,
                optimal_eps=optimal_eps,
                feasible_eps=feasible_eps,
                barrier_eps=barrier_eps,
                email=filtered_email,
                neos_url=neos_url,
                timeout=timeout,
            )
        except Exception as exc:  # noqa: BLE001  # pylint: disable=broad-exception-caught
            if quiet:
                ok, solver_name, out = (
                    False,
                    "error",
                    f"Solver error: {exc}",
                )
            else:
                raise

        screen_report, ai_report = _format_iis_report(solver_name, ok, out, full=full)
        print(screen_report)
        report_parts.append(ai_report)

        # Quiet mode: when no local solver and no NEOS email, give targeted
        # advice.
        if quiet and not available and not filtered_email:
            neos_hint = (
                f"\n{_c(_YELLOW, 'Note (quiet mode):')} "
                "No local solvers found and NEOS e-mail is not configured.\n"
                "  The static analysis above may still pinpoint the issue.\n"
                "  To enable IIS analysis:\n"
                "    \u2022 Install a local solver:  "
                "sudo apt install coinor-clp coinor-cbc\n"
                "    \u2022 Or set a NEOS e-mail:    "
                "gtopt_check_lp --init-config"
            )
            print(neos_hint)
            report_parts.append(neos_hint)
    else:
        hint = (
            f"\n{_c(_YELLOW, 'Tip:')} "
            "Install a local solver (CPLEX, HiGHS, or CLP/CBC) or provide\n"
            "  --email <address> to submit to the NEOS server for IIS "
            "analysis.\n"
            "  CLP/CBC (coinor-clp / coinor-cbc) are usually available on\n"
            "  systems where gtopt is installed: "
            "sudo apt install coinor-clp coinor-cbc"
        )
        print(hint)
        report_parts.append(hint)

    # Flush after solver analysis so the parent process receives the complete
    # static + solver output even if the AI step below hangs or is killed.
    sys.stdout.flush()

    # 3 -- AI diagnostics ────────��───────────────────────────���────────────────
    if ai_opts.enabled:
        try:
            _run_ai_diagnostics(
                report_parts,
                provider=ai_opts.provider,
                model=ai_opts.model or None,
                prompt_template=ai_opts.prompt or _AI_INFEASIBILITY_PROMPT,
                api_key=ai_opts.key or None,
                timeout=ai_opts.timeout,
            )
        except Exception as exc:  # noqa: BLE001  # pylint: disable=broad-exception-caught
            if quiet:
                print(
                    _c(
                        _YELLOW,
                        f"  Warning: AI diagnostics failed: {exc}",
                    ),
                    file=sys.stderr,
                )
            else:
                raise

    if output_file:
        _write_report(output_file, report_parts)

    return 0


