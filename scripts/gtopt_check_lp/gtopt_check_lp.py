"""
gtopt_check_lp.py — Analyze infeasible LP files generated by gtopt.

When gtopt generates an ``error*.lp`` file due to solver infeasibility, this
tool helps diagnose the cause by performing several complementary analyses:

1. **Static analysis** — parses the LP file directly and reports:
   - Variables with conflicting bounds (lb > ub)
   - Empty or fixed constraints
   - Very large or very small coefficients (numerical range issues)
   - Duplicate constraint names
   - Overall problem statistics

2. **Local IIS finder** — if a supported solver binary is on PATH, runs it to
   find the *Irreducible Infeasible Subsystem* (IIS): the minimal set of
   constraints and bounds that cannot all be satisfied simultaneously.
   Supported solvers (tried in order):
   - CPLEX        (``cplex`` binary)
   - HiGHS        (``highs`` binary or ``highspy`` Python package)
   - COIN-OR CLP  (``clp`` binary — same solver gtopt uses internally)
   - COIN-OR CBC  (``cbc`` binary — CLP + branch-and-cut)
   - GLPK         (``glpsol`` binary)

3. **NEOS server** — if no local solver is available, submits the LP to the
   NEOS online optimization server (https://neos-server.org) using the
   CPLEX LP solver via its XML-RPC API, and waits for the results.

Usage
-----
::

    gtopt-check-lp problem.lp
    gtopt-check-lp problem.lp --analyze-only
    gtopt-check-lp problem.lp --solver coinor
    gtopt-check-lp problem.lp --use-neos --email user@example.com
    gtopt-check-lp problem.lp --output report.txt --verbose

Options
-------
``--analyze-only``
    Perform only the static analysis; do not invoke any solver.

``--solver {auto,cplex,highs,coinor,glpk,neos}``
    Select the IIS/infeasibility solver.  Default: ``auto`` (tries local
    solvers in order: CPLEX → HiGHS → COIN-OR → GLPK, then falls back to
    NEOS if ``--email`` is provided).

``--email EMAIL``
    E-mail address for the NEOS server submission (required for NEOS).

``--solver-url URL``
    XML-RPC endpoint for a NEOS-compatible server.
    Default: ``https://neos-server.org:3333``.

``--timeout SECONDS``
    Maximum time to wait for a solver result (default: 10 s).

``--output FILE``
    Write the full analysis report to *FILE* in addition to stdout.

``--no-color``
    Disable ANSI color codes in the output (auto-detected for non-TTY).

``--verbose``, ``-v``
    Enable verbose / debug logging.

``--version``
    Print the package version and exit.

Exit codes
----------
0 — analysis completed successfully (infeasibility cause may or may not have
    been identified; check the report).
1 — fatal error (file not found, solver crashed, network error, etc.).
"""

